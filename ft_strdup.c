#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
    size_t  len;
    char    *dup;
    size_t  i;

    len = 0;
    len = ft_strlen(s1);

    dup = (char *)malloc(len + 1);
    if (!dup)
        return (NULL);
    i = 0;
    while (i < len)
    {
        dup[i] = s1[i];
        i++;
    }
    dup[i] = '\0';
    return (dup);
}
